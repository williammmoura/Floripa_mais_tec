let num1 = 2;
let num2 = 5;

let soma = num1 + num2;
console.log(`${num1} + ${num2} = ${soma}`);

let sub = num1 - num2;
console.log(`${num1} - ${num2} = ${sub}`);

let mult = num1 * num2;
console.log(`${num1} * ${num2} = ${mult}`);

let div = num1 / num2;
console.log(`${num1} / ${num2} = ${div}`);

let exp = num1 ** num2;
console.log(`${num1} ^ ${num2} = ${exp}`);

let resto = num1 % num2;
console.log(`O resto da divisão entre ${num1} e ${num2} é: ${resto}`);